class Traffic {
    public static int numberOfVehicles(int vehicles) {
        return vehicles;
    }
    public static String trafficType(String type) {
        return type;
    }
    public static boolean isHeavy(boolean heavy) {
        return heavy;
    }
    public static long averageDelay(long minutes) {
        return minutes;
    }
    public static double roadLength(double km) {
        return km;
    }
    public static float trafficRating(float rating) {
        return rating;
    }
}