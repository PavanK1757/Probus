class Book {
    public static String[] bookName(String[] name) { return name; }
    public static int[] bookPages(int[] pages) { return pages; }
    public static double[] bookRatings(double[] ratings) { return ratings; }
    public static float[] bookPrice(float[] price) { return price; }
    public static boolean[] bookIsAvailable(boolean[] available) { return available; }
    public static char[] bookSymbol(char[] symbol) { return symbol; }
    public static long[] bookISBN(long[] isbn) { return isbn; }
}

