class Fruits {
    public static String[] fruitName(String[] name) { return name; }
    public static int[] fruitPrice(int[] price) { return price; }
    public static double[] fruitWeight(double[] weight) { return weight; }
    public static float[] fruitQty(float[] qty) { return qty; }
    public static boolean[] fruitIsFresh(boolean[] fresh) { return fresh; }
    public static char[] fruitSymbol(char[] symbol) { return symbol; }
    public static long[] fruitCode(long[] code) { return code; }
}

