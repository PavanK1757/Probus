class Building {
    public static String[] buildingName(String[] name) {
        return name;
    }
    public static int[] buildingFloors(int[] floors) {
        return floors;
    }
    public static double[] buildingHeight(double[] height) {
        return height;
    }
    public static float[] buildingArea(float[] area) {
        return area;
    }
    public static boolean[] buildingIsCommercial(boolean[] type) {
        return type;
    }
    public static char[] buildingSymbol(char[] symbol) {
        return symbol;
    }
    public static long[] buildingNumber(long[] number) {
        return number;
    }
}